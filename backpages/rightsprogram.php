                      <td colspan="3"><p style="text-align: justify;">MANTENIMIENTO: definido como el conjunto de operaciones para que un equipamiento reúna las condiciones para el propósito para el que fue construido.<br>
                      1. Mantenimiento de conservación: es el destinado a compensar el deterioro sufrido por el uso, los agentes meteorológicos u otras causas. En el mantenimiento de conservación pueden diferenciarse:<br>
                      1.1 Mantenimiento correctivo: que corrige los defectos o averías observados. Como su nombre bien lo indica es el mantenimiento encargado de corregir una falla que se presente en determinado momento. Este tipo de mantenimiento se realiza en las instalaciones físicas . Las etapas por seguir cuando se presente un problema de mantenimiento correctivo, pueden ser:<br>
                      - Identificar el problema y sus causas<br>                 - Estudiar las diferentes alternativas para su reparación<br>                    - Evaluar las ventajas de cada alternativa y escoger la optima<br>                   - Planear la reparación de acuerdo con el personal y equipos disponibles<br>                    - Supervisar las actividades por desarrollar<br>
                      1.1.1  Mantenimiento correctivo inmediato: es el que se realiza inmediatamente de percibir la avería y defecto, con los medios disponibles, destinados a ese fin.<br>
                      1.1.2 Mantenimiento correctivo diferido: al producirse la avería o defecto, se produce un paro de la instalación o equipamiento de que se trate, para posteriormente afrontar la reparación, solicitándose los medios para ese fin.<br>
                      1.2 Mantenimiento preventivo: Es el destinado a garantizar la fiabilidad de equipos, maquinarias, etc., en funcionamiento antes de que pueda producirse un accidente o avería por deterioro. Su escencia radica en las revisiones e inspecciones programadas  que pueden o no tener como consecuencia una tarea correctiva o de cambio. Este sistema se basa en el hecho de que las partes de un equipo y/o maquinaria se gasten en forma desigual  y es necesario presentarle prestarle servicios en forma racional para garantizar su buen funcionamiento. El mantenimiento preventivo se realiza mediante un programa de actividades (revisiones, lubricaciones, inspecciones), previamente establecidas con el fin de anticiparse a la presencia de fallas en instalaciones y equipos. Lo anterior se realiza mediante una programación previa de actividades con el fin de evitar en lo posible la mayor cantidad de imprevistos disminuyendo tiempo y costos .Durante el desarrollo del mantenimiento preventivo es importante tener en cuenta:<br>
                      - Que hay que mantener <br>                                            - Cuando y cada cuanto hay que hacerlo   <br>                                                - Con que personal se cuenta para hacerlo<br>                                                          - Que hay que hacer para mantenerlo en funcionamiento<br>                                                       - Que tiempo se cuenta para hacerlo<br>                                                             - Como hay que hacerlo                                                                                                                                                              </p></td>
                    </tr>

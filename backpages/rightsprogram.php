                      <td colspan="3">
                        <p style="text-align: justify;">
                        VENTAJAS DEL MANTENIMIENTO: Con un adecuado mantenimiento preventivo, las instalaciones, maquinaria, vehículos, equipos y accesorios se conservarán en optimas condiciones de trabajo, permitiendo que el servicio continué su flujo normal sin interrupciones. Las personas que laboran con estos equipos, maquinarias y vehículos se sentirán mas satisfechas y trabajaran con un mayor grado de motivación.<br>
                        1.2.1 En el mantenimiento preventivo podemos ver:<br>
                        1.2.1.1 Mantenimiento programado: como el que se realiza por programa de revisiones, por tiempo de funcionamiento, kilometraje, etc.<br>
                        1.2.1.2 Mantenimiento predictivo: que realiza las intervenciones prediciendo el momento que el equipo quedara fuera de servicio mediante un seguimiento de su funcionamiento determinando su evolución, y por tanto el momento en el que las reparaciones deben efectuarse.<br>
                        1.2.1.3 Mantenimiento de oportunidad: que es el que aprovecha las paradas o periodos de no uso de los equipos para realizar las operaciones de mantenimiento, realizando las revisiones o reparaciones necesarias para garantizar el buen funcionamiento de los equipos en el nuevo periodo de utilización.<br>
                        El mantenimiento preventivo se registra en el formato tabla de frecuencia en el cual se describe el mantenimiento programado a realizar.<br>
                        2. Mantenimiento de actualización: cuyo propósito es compensar la obsolescencia tecnológica, o las nuevas exigencias, que en el momento de construcción no existían o no fueron tenidas en cuenta pero que en la actualidad si tienen que serlo.<br>

